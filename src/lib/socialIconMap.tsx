interface SocialIconMap {
  [key: string]: React.ComponentType<{ size?: number }> | LucideIcon;
}

export const socialIconMap: SocialIconMap = {
  instagram: Instagram,
  linkedin: Linkedin,
  facebook: Facebook,
  twitter: Twitter,
  youtube: Youtube,
  github: Github,
  pinterest: PinterestIcon,
  tiktok: TikTokIcon,
  whatsapp: WhatsAppIcon,
};

/**
 * Get the appropriate icon component for a social network
 * @param networkName - Name of the social network (lowercase)
 * @returns Icon component or Globe as fallback
 */
export const getSocialIcon = (networkName: string): React.ComponentType<{ size?: number }> | LucideIcon => {
  const normalizedName = networkName.toLowerCase().trim();
  return socialIconMap[normalizedName] || Globe;
};
